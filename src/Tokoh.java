
public class Tokoh {
	final String nama;
    final String JK;
    final String pekerjaan;

    public Tokoh(String nama, String JK, String pekerjaan) {
        this.nama = nama;
        this.JK = JK;
        this.pekerjaan = pekerjaan;
    }

    public String getNama() {
        return nama;
    }

    public String getJK() {
        return JK;
    }

    public String getPekerjaan() {
        return pekerjaan;
    }

}
